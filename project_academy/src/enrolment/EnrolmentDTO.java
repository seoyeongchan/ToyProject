package enrolment;

public class EnrolmentDTO {
	private int idx;
	private int member_idx;
	private int timetable_idx;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getMember_idx() {
		return member_idx;
	}
	public void setMember_idx(int member_idx) {
		this.member_idx = member_idx;
	}
	public int getTimetable_idx() {
		return timetable_idx;
	}
	public void setTimetable_idx(int timetable_idx) {
		this.timetable_idx = timetable_idx;
	}	
}
